import { mkdir, writeFile } from 'fs/promises';
import { PrismaClient } from '@prisma/client';

const frontendBase = process.env.FRONTEND_BASE || 'http://127.0.0.1:3001';
const backendBase = process.env.BACKEND_BASE || 'http://127.0.0.1:3000';
const outputDir = new URL('../reports/sprint-10/evidence/', import.meta.url);
const prisma = new PrismaClient();

const credentials = {
  admin: { email: 'or.peretz@demo.com', password: 'password123' },
  resident: { email: 'client@demo.com', password: 'password123' },
  tech: { email: 'tech1@demo.com', password: 'password123' },
};

const results = [];

async function main() {
  await mkdir(outputDir, { recursive: true });

  await verifyCorsPreflight();

  const admin = await login('admin', credentials.admin);
  const resident = await login('resident', credentials.resident);
  const tech = await login('tech', credentials.tech);

  await verifyProtectedRoute(admin.accessToken);
  await verifyRefresh(admin.refreshToken);
  await verifyDocumentUpload(admin.accessToken);
  await verifyTicketUpload(resident.accessToken);
  await verifyWorkOrderPhotoUpload(tech.accessToken);

  const payload = {
    generatedAt: new Date().toISOString(),
    frontendBase,
    backendBase,
    results,
  };

  await writeFile(new URL('integration-results.json', outputDir), JSON.stringify(payload, null, 2));
  await prisma.$disconnect();
  console.log(`Sprint 10 verification complete. Wrote ${results.length} checks.`);
}

async function verifyCorsPreflight() {
  const response = await fetch(`${backendBase}/api/v1/documents`, {
    method: 'OPTIONS',
    headers: {
      Origin: frontendBase,
      'Access-Control-Request-Method': 'POST',
      'Access-Control-Request-Headers': 'authorization,content-type',
    },
  });

  record('cors-preflight', response, {
    allowOrigin: response.headers.get('access-control-allow-origin'),
    allowMethods: response.headers.get('access-control-allow-methods'),
    allowHeaders: response.headers.get('access-control-allow-headers'),
  });

  assert(response.ok, 'CORS preflight failed');
}

async function login(label, creds) {
  const response = await fetch(`${frontendBase}/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(creds),
  });
  const body = await parseBody(response);
  record(`login-${label}`, response, redactTokens(body));
  assert(response.ok, `Login failed for ${label}`);
  return body;
}

async function verifyProtectedRoute(accessToken) {
  const unauthorized = await fetch(`${frontendBase}/api/v1/dashboard`);
  record('dashboard-unauthorized', unauthorized, await parseBody(unauthorized));
  assert(unauthorized.status === 401, 'Protected route should reject requests without auth');

  const authorized = await fetch(`${frontendBase}/api/v1/dashboard`, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  record('dashboard-authorized', authorized, await parseBody(authorized));
  assert(authorized.ok, 'Protected route should work through frontend rewrite');
}

async function verifyRefresh(refreshToken) {
  const response = await fetch(`${frontendBase}/auth/refresh`, {
    method: 'POST',
    headers: { Authorization: `Bearer ${refreshToken}` },
  });
  const body = await parseBody(response);
  record('refresh-token', response, redactTokens(body));
  assert(response.ok && body.accessToken, 'Refresh token flow failed');
}

async function verifyDocumentUpload(accessToken) {
  const form = new FormData();
  const content = `Sprint 10 document upload check ${new Date().toISOString()}\n`;
  form.append('file', new Blob([content], { type: 'text/plain' }), 'sprint-10-document.txt');
  form.append('name', 'Sprint 10 integration document');
  form.append('category', 'Verification');
  form.append('description', 'Generated by scripts/verify-sprint-10.mjs');
  form.append('accessLevel', 'PRIVATE');

  const upload = await fetch(`${frontendBase}/api/v1/documents/upload`, {
    method: 'POST',
    headers: { Authorization: `Bearer ${accessToken}` },
    body: form,
  });
  const body = await parseBody(upload);
  record('document-upload', upload, body);
  assert(upload.ok && body.url, 'Document upload failed');

  const fileResponse = await fetch(`${backendBase}${body.url}`);
  const fileText = await fileResponse.text();
  record('document-upload-fetch', fileResponse, { body: fileText });
  assert(fileResponse.ok, 'Uploaded document is not retrievable from backend uploads path');
  assert(fileText.includes('Sprint 10 document upload check'), 'Uploaded document content mismatch');
}

async function verifyTicketUpload(accessToken) {
  const buildingsResponse = await fetch(`${frontendBase}/api/v1/buildings`);
  const buildings = await parseBody(buildingsResponse);
  record('buildings-for-ticket', buildingsResponse, summarizeArray(buildings));
  assert(buildingsResponse.ok && Array.isArray(buildings) && buildings.length > 0, 'No buildings available for ticket check');

  const admin = await login('admin-ticket-scan', credentials.admin);
  const unitsResponse = await fetch(`${frontendBase}/api/v1/units?buildingId=${buildings[0].id}`, {
    headers: { Authorization: `Bearer ${admin.accessToken}` },
  });
  const units = await parseBody(unitsResponse);
  record('units-for-ticket', unitsResponse, summarizeArray(units));
  assert(unitsResponse.ok && Array.isArray(units) && units.length > 0, 'No units available for ticket creation');

  const form = new FormData();
  form.append('unitId', String(units[0].id));
  form.append('severity', 'HIGH');
  form.append('description', 'Sprint 10 resident upload verification ticket');
  form.append('photos', new Blob(['fake image payload'], { type: 'image/png' }), 'sprint-10-photo.png');

  const create = await fetch(`${frontendBase}/api/v1/tickets`, {
    headers: { Authorization: `Bearer ${accessToken}` },
    method: 'POST',
    headers: { Authorization: `Bearer ${accessToken}` },
    body: form,
  });
  const ticket = await parseBody(create);
  record('ticket-create-with-photo', create, {
    id: ticket?.id,
    photoCount: Array.isArray(ticket?.photos) ? ticket.photos.length : 0,
  });
  assert(create.ok && Array.isArray(ticket.photos) && ticket.photos.length > 0, 'Ticket photo upload failed');

  const ticketPhoto = await fetch(normalizeUploadUrl(ticket.photos[0]));
  record('ticket-photo-fetch', ticketPhoto, { url: ticket.photos[0] });
  assert(ticketPhoto.ok, 'Uploaded ticket photo is not retrievable');
}

async function verifyWorkOrderPhotoUpload(accessToken) {
  let list = await fetch(`${frontendBase}/api/v1/work-orders`, {
    headers: { Authorization: `Bearer ${accessToken}` },
  });
  let workOrders = await parseBody(list);
  record('work-orders-list', list, summarizeArray(workOrders));

  if (!Array.isArray(workOrders) || workOrders.length === 0) {
    const seeded = await ensureWorkOrderFixture();
    record('work-order-seed-fixture', { ok: true, status: 200 }, seeded);

    list = await fetch(`${frontendBase}/api/v1/work-orders`, {
      headers: { Authorization: `Bearer ${accessToken}` },
    });
    workOrders = await parseBody(list);
    record('work-orders-list-after-seed', list, summarizeArray(workOrders));
  }

  assert(list.ok && Array.isArray(workOrders) && workOrders.length > 0, 'No work orders available for photo upload check');

  const target = workOrders[0];
  const form = new FormData();
  form.append('photos', new Blob(['fake image payload'], { type: 'image/jpeg' }), 'sprint-10-work-order.jpg');

  const update = await fetch(`${frontendBase}/api/v1/work-orders/${target.id}/photos`, {
    method: 'PATCH',
    headers: { Authorization: `Bearer ${accessToken}` },
    body: form,
  });
  const body = await parseBody(update);
  record('work-order-photo-upload', update, {
    id: body?.id,
    photoCount: Array.isArray(body?.photos) ? body.photos.length : 0,
  });
  assert(update.ok && Array.isArray(body.photos) && body.photos.length > 0, 'Work-order photo upload failed');

  const uploaded = body.photos[body.photos.length - 1];
  const uploadedResponse = await fetch(normalizeUploadUrl(uploaded));
  record('work-order-photo-fetch', uploadedResponse, { url: uploaded });
  assert(uploadedResponse.ok, 'Uploaded work-order photo is not retrievable');
}

async function ensureWorkOrderFixture() {
  let supplier = await prisma.supplier.findFirst({
    orderBy: { id: 'asc' },
  });

  if (!supplier) {
    supplier = await prisma.supplier.create({
      data: {
        name: 'Sprint 10 Verification Supplier',
        skills: ['Verification'],
        documents: [],
      },
    });
  }

  const ticket = await prisma.ticket.findFirst({
    orderBy: { id: 'asc' },
  });
  assert(ticket, 'No tickets available to seed a work order fixture');

  let workOrder = await prisma.workOrder.findFirst({
    where: { ticketId: ticket.id },
    orderBy: { id: 'asc' },
  });

  if (!workOrder) {
    workOrder = await prisma.workOrder.create({
      data: {
        ticketId: ticket.id,
        supplierId: supplier.id,
        costEstimate: 250,
        photos: [],
      },
    });
  }

  return { supplierId: supplier.id, ticketId: ticket.id, workOrderId: workOrder.id };
}

function normalizeUploadUrl(value) {
  if (typeof value !== 'string') {
    throw new Error('Upload URL is missing');
  }
  if (value.startsWith('http://') || value.startsWith('https://')) {
    return value;
  }
  return `${backendBase}${value}`;
}

function summarizeArray(value) {
  return Array.isArray(value)
    ? { count: value.length, firstId: value[0]?.id ?? null }
    : value;
}

function redactTokens(value) {
  if (!value || typeof value !== 'object') {
    return value;
  }
  return {
    ...value,
    accessToken: value.accessToken ? '[redacted]' : value.accessToken,
    refreshToken: value.refreshToken ? '[redacted]' : value.refreshToken,
  };
}

async function parseBody(response) {
  const contentType = response.headers.get('content-type') || '';
  if (contentType.includes('application/json')) {
    return response.json();
  }
  return response.text();
}

function record(name, response, body) {
  results.push({
    name,
    status: response.status,
    ok: response.ok,
    body,
  });
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

main().catch((error) => {
  console.error(error);
  prisma.$disconnect().catch(() => {});
  process.exitCode = 1;
});
