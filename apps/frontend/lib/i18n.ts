export function normalizeLocale(locale?: string | null): Locale {
  if (!locale) {
    return 'he';
  }

  return locale.toLowerCase().startsWith('en') ? 'en' : 'he';
}

export function getIntlLocale(locale?: string | null): string {
  return localeMeta[normalizeLocale(locale)].intl;
}

export function getLocaleDirection(locale?: string | null): 'rtl' | 'ltr' {
  return localeMeta[normalizeLocale(locale)].direction;
}

export function getDateFnsLocale(locale?: string | null) {
  return normalizeLocale(locale) === 'en' ? enUS : he;
}

export function getStoredLocale(defaultLocale: Locale = 'he'): Locale {
  if (typeof window === 'undefined') {
    return defaultLocale;
  }

  const stored = window.localStorage.getItem('amit-locale');
  if (stored === 'he' || stored === 'en') {
    return stored;
  }

  const htmlLang = window.document.documentElement.lang;
  if (htmlLang) {
    return normalizeLocale(htmlLang);
  }

  return normalizeLocale(window.navigator.language);
}

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const dictionary = translations[locale] ?? translations.he;
  let text = dictionary[key] ?? translations.he[key] ?? key;

  if (params) {
    for (const [paramKey, paramValue] of Object.entries(params)) {
      text = text.replace(`{{${paramKey}}}`, String(paramValue));
    }
  }

  return text;
}
